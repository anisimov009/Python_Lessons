# 1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

value = 15 * 3
print('15 * 3 = ', value, type(value))

value = 15 / 3
print('15 / 3 = ', value, type(value))

value = 15 // 3
print('15 // 3 = ', value, type(value))

value = 15 ** 2
print('15 ** 2 = ', value, type(value))


